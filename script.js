const container = document.getElementById("animeContainer");
const search = document.getElementById("search");
const buttons = document.querySelectorAll(".category-btn");

let selectedCategory = "all";
let searchText = "";

function renderAnime() {

container.innerHTML = "";

const filteredAnime = animeData.filter(anime => {

const matchCategory =  
  selectedCategory === "all" ||  
  anime.category === selectedCategory;  

const matchSearch =  
  anime.title.toLowerCase().includes(searchText);  

return matchCategory && matchSearch;

});

filteredAnime.forEach(anime => {

const card = document.createElement("div");  

card.classList.add("card");  

card.innerHTML = `

  <img src="${anime.img}" alt="${anime.title}">

  <h3>${anime.title}</h3>

  <p class="rating">⭐ ${anime.rating}</p>

  <p class="episodes">${anime.episodes}</p>

<div class="genres">
  ${anime.genre.map(g => `<span>${g}</span>`).join("")}
</div>

  <button class="fav-btn">
    ❤️ Favorite
  </button>

`;
  
card.addEventListener("click", () => {  

  document.getElementById("animeModal").style.display = "block";  

  document.getElementById("modalImg").src = anime.img;  

  document.getElementById("modalTitle").innerText = anime.title;  

  document.getElementById("modalCategory").innerText =  
    "Category: " + anime.category;  

  document.getElementById("modalDescription").innerText =  
    anime.description;  

  document.getElementById("watchBtn").href = anime.link;

document.getElementById("watchBtn").onclick = () => {

let watching =
JSON.parse(localStorage.getItem("watching")) || [];

if (!watching.includes(anime.title)) {

watching.unshift(anime.title);  

localStorage.setItem(  
  "watching",  
  JSON.stringify(watching)  
);  

renderContinueWatching();

}

};

});

const favBtn = card.querySelector(".fav-btn");

favBtn.addEventListener("click", (e) => {

e.stopPropagation();

let favorites =
JSON.parse(localStorage.getItem("favorites")) || [];

if (!favorites.includes(anime.title)) {

favorites.push(anime.title);  

localStorage.setItem(  
  "favorites",  
  JSON.stringify(favorites)  
);  

favBtn.innerText = "✅ Added";

}

});

container.appendChild(card);

});

}

search.addEventListener("input", (e) => {

  searchText = e.target.value.toLowerCase();

  renderAnime();

  const suggestionsBox =
    document.getElementById("suggestionsBox");

  suggestionsBox.innerHTML = "";

  if (searchText.length > 0) {

    const matchedAnime = animeData.filter(anime =>
      anime.title.toLowerCase().includes(searchText)
    );

    matchedAnime.forEach(anime => {

      const item = document.createElement("div");

      item.classList.add("suggestion-item");

      item.innerText = anime.title;

      item.addEventListener("click", () => {

        search.value = anime.title;

        searchText = anime.title.toLowerCase();

        renderAnime();

        suggestionsBox.innerHTML = "";

      });

      suggestionsBox.appendChild(item);

    });

  }

});

buttons.forEach(btn => {

btn.addEventListener("click", () => {

buttons.forEach(b => b.classList.remove("active"));  

btn.classList.add("active");  

selectedCategory = btn.dataset.category;  

renderAnime();

});

});

const modal = document.getElementById("animeModal");

const closeBtn = document.querySelector(".close-btn");

closeBtn.addEventListener("click", () => {

modal.style.display = "none";
  document.getElementById("trailerFrame").src = "";

});

window.addEventListener("click", (e) => {

if (e.target === modal) {

modal.style.display = "none";
  document.getElementById("trailerFrame").src = "";

}

});

const exploreBtn = document.getElementById("exploreBtn");

exploreBtn.addEventListener("click", () => {

document
.querySelector(".anime-container")
.scrollIntoView({
behavior: "smooth"
});

});

renderAnime();
const featuredList = [
"Naruto",
"Attack on Titan",
"One Piece",
"Demon Slayer",
"Jujutsu Kaisen",
"Tokyo Ghoul",
"Chainsaw Man"
];

let currentFeatured = 0;

setInterval(() => {

currentFeatured++;

if (currentFeatured >= featuredList.length) {
currentFeatured = 0;
}

document.getElementById("featuredAnime").innerText =
featuredList[currentFeatured];

}, 2500);
function renderFavorites() {

const favoritesContainer =
document.getElementById("favoritesContainer");

favoritesContainer.innerHTML = "";

const favorites =
JSON.parse(localStorage.getItem("favorites")) || [];

favorites.forEach(fav => {

const item = document.createElement("div");  

item.classList.add("favorite-item");  

item.innerText = fav;  

favoritesContainer.appendChild(item);

});

}

renderFavorites();

function renderContinueWatching() {

const continueContainer =
document.getElementById("continueContainer");

continueContainer.innerHTML = "";

const watching =
JSON.parse(localStorage.getItem("watching")) || [];

watching.forEach(anime => {

const item = document.createElement("div");  

item.classList.add("continue-item");  

item.innerText = anime;  

continueContainer.appendChild(item);

});

}

renderContinueWatching();
